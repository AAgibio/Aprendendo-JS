let contador = 1

while (contador <= 10) {
    console.log(`contador = ${contador}`);
    contador++
} 

for (let i = 1; i <= 10; i++) {
    console.log(`i = ${i}`);
}


const notas = [6.5, 4.5, 1.3]
for (let i = 0; i < notas.length; i++) {
    console.log(`Nota ${i} = ${notas[i]}`);
}